public class Switch
{
	public static void main(String[] args)
	{
		String a="yellow";
		switch(a)
		{
			case "red":
			System.out.println("It is red color");
			break;
			case "yellow":
			System.out.println("It is Yellow color");
			break;
			default:
			System.out.println("Enter a valid color");
		}
	}
}